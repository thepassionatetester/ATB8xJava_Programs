package ex_13092024;

public class Mathematical_Calculator {
    public static void main(String[] args) {
        double a = 10;
        double b = 20;

        double addition = a + b;
        double subtraction = b-a;
        double multiplication = a*b;
        double division = b / a;
        double modulus = b % a;

        System.out.println("Program for simple mathematical calculator.");
        System.out.printf("Addition result %f", addition);
        System. out.println(' ');
        System.out.printf("Subtraction result %f", subtraction);
        System. out.println(' ');
        System.out.printf("Multiplication result %f", multiplication);
        System. out.println(' ');
        System.out.printf("Division result %f", division);
        System. out.println(' ');
        System.out.printf("Modulus  result %f", modulus);
    }
}
